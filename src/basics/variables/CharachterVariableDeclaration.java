package basics.variables;

public class CharachterVariableDeclaration {
    public static void main(String[] args) {
        // a literal character
        char myChar = 'K';
        // a unicode value
        char myUnicodeValue = '\u0044';
        // a integer value
        char myIntegerValueChar = 68;

        System.out.println("Literal character is: " + myChar);
        System.out.println("Unicode character is: " + myUnicodeValue);
        System.out.println("Integer value character is: " + myIntegerValueChar);

    }
}
