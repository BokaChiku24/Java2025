package basics.statements;

public class Stamenet {
    public static void main(String[] args){
        int myVariable = 50; // One statement in java. Line ends with semicolon ; is a statement

        myVariable++; // java statement
        myVariable--; // java statement

        System.out.println("This is the statement"); // java statement

        System.out.println("This is" +
                " another " +
                "statement");
        // from line 12 to 14 is the java statement

        int mySecoundVariable = 100; mySecoundVariable++; System.out.println(mySecoundVariable);
        // line number 17 is also a valid java statement
    }
}
