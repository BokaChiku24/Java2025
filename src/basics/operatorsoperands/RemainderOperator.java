package basics.operatorsoperands;

public class RemainderOperator {
    public static void main(String[] args) {
        int myNumber = 10;
        int mySecondNumber = 2;
        System.out.println("Division result is:" + (myNumber / mySecondNumber));
        System.out.println("Reminder result is:" + (myNumber % mySecondNumber));
        System.out.println("Reminder result is:" + (myNumber % 3));
    }
}
