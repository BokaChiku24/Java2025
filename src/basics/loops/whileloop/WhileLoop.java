package basics.loops.whileloop;

public class WhileLoop {
    public static void main(String[] args) {
        for (int i = 0; i <= 10; i++) {
            System.out.println(i);
        }

        // while loop example
        int j = 0;
        while (j <= 10) {
            System.out.println(j);
            j++;
        }
    }

}
