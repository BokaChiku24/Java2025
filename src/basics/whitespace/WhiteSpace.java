package basics.whitespace;

public class WhiteSpace {
    public static void main(String[] args){
        int mySecoundVariable = 100; mySecoundVariable++; System.out.println(mySecoundVariable);

        // same code like this
        int myThirdVariable = 100;
        myThirdVariable++;
        System.out.println(myThirdVariable);

        // whitespace
        int                                     myWhiteSpace
                      =
                                  100
                                          ;
        //line number 13 to 16 white space
        System.out.println(myWhiteSpace);
    }
}
